public class VisitEntity {
    private int comments;
    private int danmakuSize;
    private int goldBanana;
    private int score;
    private int stows;
    private int ups;
    private int views;

    public int getComments() {
        return comments;
    }

    public void setComments(int comments) {
        this.comments = comments;
    }

    public int getDanmakuSize() {
        return danmakuSize;
    }

    public void setDanmakuSize(int danmakuSize) {
        this.danmakuSize = danmakuSize;
    }

    public int getGoldBanana() {
        return goldBanana;
    }

    public void setGoldBanana(int goldBanana) {
        this.goldBanana = goldBanana;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getStows() {
        return stows;
    }

    public void setStows(int stows) {
        this.stows = stows;
    }

    public int getUps() {
        return ups;
    }

    public void setUps(int ups) {
        this.ups = ups;
    }

    public int getViews() {
        return views;
    }

    public void setViews(int views) {
        this.views = views;
    }
}
